const char* rfind(const char* haystack, char needl){
	int i = 0;
	int lastpos = strlen(haystack) - 1;
	for(i = lastpos; i >= 0; i--){
		if(haystack[i] == needl)
			return haystack + i;
	}
	return NULL;
}

void appendClass(const char* class){
	strcat(wantedClasses, "import ");
	strcat(wantedClasses, class);
	strcat(wantedClasses, ";\n");
}

